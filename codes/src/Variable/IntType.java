package Variable;

public class IntType {
    public static void main(String[] args) {
        int temp = 10;      // 10진수로 초기화
        int temp2 = 012;    // 8진수로 초기화
        int temp3 = 0xA;    // 16진수로 초기화

        System.out.println(temp);
        System.out.println(temp2);
        System.out.println(temp3);
    }
}
