package Variable;

public class Variable {
    public static void main(String[] args) {
        // 정수형 변수 value 를 10으로 초기화
        int value = 10;

        // 정수형 변수 result 에 변수 value 와 10을 더한 값을 초기화
       int result = value + 10;

       // 변수 result 의 값을 출력
        System.out.println(result);
    }
}
