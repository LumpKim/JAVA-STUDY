package Variable;

public class LongType {
    public static void main(String[] args) {
        long temp = 10;
        long temp2 = 100L;
        // long temp3 = 1000000000000;  에러 발생
        long temp4 = 1000000000000L;

        System.out.println(temp);
        System.out.println(temp2);
        System.out.println(temp4);
    }
}
